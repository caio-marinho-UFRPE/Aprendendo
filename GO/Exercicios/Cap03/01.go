package main

import "fmt"

// Usando o operador curto de declaração dê os seguintes valores às variáveis:
// x = 42, y = "James Bond", z = true e depois imprima com uma única declaração
// de print e depois múltiplas declarações
func exC301() {
	x0301 := 42
	y0302 := "James Bond"
	z0303 := true

	fmt.Println("O ator do novo filme do", y0302, "tem", x0301, "anos de idade? É sério?", z0303)

	fmt.Println("x:", x0301)
	fmt.Println("y:", y0302)
	fmt.Println("z:", z0303)
}
